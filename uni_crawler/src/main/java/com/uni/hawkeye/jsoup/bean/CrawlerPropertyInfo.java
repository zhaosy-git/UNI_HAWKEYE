package com.uni.hawkeye.jsoup.bean;

public class CrawlerPropertyInfo {

	private String cookie_from;
	private String cookie;
	private String token;

	/**
	 * @return the cookie_from
	 */
	public String getCookie_from() {
		return cookie_from;
	}

	/**
	 * @param cookie_from
	 *            the cookie_from to set
	 */
	public void setCookie_from(String cookie_from) {
		this.cookie_from = cookie_from;
	}

	/**
	 * @return the cookie
	 */
	public String getCookie() {
		return cookie;
	}

	/**
	 * @param cookie
	 *            the cookie to set
	 */
	public void setCookie(String cookie) {
		this.cookie = cookie;
	}

	/**
	 * @return the token
	 */
	public String getToken() {
		return token;
	}

	/**
	 * @param token
	 *            the token to set
	 */
	public void setToken(String token) {
		this.token = token;
	}

}
